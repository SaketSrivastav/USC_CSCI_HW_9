public class Hotel {
	String hotelName;
	String hotelLocation;
	String hotelImageUrl;
	String hotelReviewNo;
	String hotelRatings;
	String hotelReviewUrl;;
	
	public String getHotelLocation() {
		return hotelLocation;
	}

	public void setHotelLocation(String hotelLocation) {
		this.hotelLocation = hotelLocation;
	}

	public String getHotelName() {
		return hotelName;
	}
	
	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}
	
	public String getHotelImageUrl() {
		return hotelImageUrl;
	}
	
	public void setHotelImageUrl(String hotelImageUrl) {
		this.hotelImageUrl = hotelImageUrl;
	}
	
	public String getHotelReviewNo() {
		return hotelReviewNo;
	}
	
	public void setHotelReviewNo(String hotelReviewNo) {
		this.hotelReviewNo = hotelReviewNo;
	}
	
	public String getHotelRatings() {
		return hotelRatings;
	}
	
	public void setHotelRatings(String hotelRatings) {
		this.hotelRatings = hotelRatings;
	}
	
	public String getHotelReviewUrl() {
		return hotelReviewUrl;
	}
	
	public void setHotelReviewUrl(String hotelReviewUrl) {
		this.hotelReviewUrl = hotelReviewUrl;
	}

}
